str1 = str.upper(input())
morse = [".-", "-...", "-.-.", "-..", ".", "..-.", "--.", "....", "..", ".---", "-.-", ".-..", "--", "-.", "---", ".--.", "--.-", ".-.", "...", "-", "..-", "...-", ".--", "-..-", "-.--", "--.."]
digit = ['-----', '.----', '..---', '...--', '....-', '.....', '-....', '--...', '---..', '----.']
punctuation = {'.': '.-.-.-', ':': '---...', ',': '--..--', ';': '-.-.-.', '?': '..- -..', '=': '-...-', "'": '.----.', '/': '-..-.', '!': '-.-.--', '-': '-....-', '_': '..--.-', '"': '.-..-.', '(': '-.--.', ')': '-.--.-', '$': '...-..-', '&': '路-路路路', '@': '.--.-.', ' ': ''}

for ch in str1:
    if ord('A') <= ord(ch) <= ord('Z'):
        print(morse[ord(ch)-ord('A')],end=' ')
    elif ord('0')<=ord(ch)<=ord('9'):
        print(digit[ord(ch)-ord('0')],end=' ')
    elif ch in punctuation.keys():
        print(punctuation[ch],end=' ')
    else:
        print(ch,end=' ')